#include "c.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_rest(t_lolo *x, t_format *r)
{
	if (x->lo[x->i] && x->lo[x->i] == '%')
	{
		x->z = 0;
		x->i++;
		while (x->lo[x->i] == ' ')
		{
			x->z++;
			x->i++;
		}
		x->i = ft_small(r, x->ap, x, x->z);
	}
}
